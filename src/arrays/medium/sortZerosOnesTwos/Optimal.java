package arrays.medium.sortZerosOnesTwos;

public class Optimal
{
    public static void swap(int[] nums, int start, int end)
    {
        int temp = nums[start];
        nums[start] = nums[end];
        nums[end] = temp;
    }

    private static void sort(int[] nums)
    {
        int n = nums.length;

        int start = 0;
        int mid = 0;
        int end = n - 1;

        while(mid <= end)
        {
            if(nums[mid] == 0)
            {
                swap(nums, start, mid);
                start++;
                mid++;
            }
            else if(nums[mid] == 1)
            {
                mid++;
            }
            else
            {
                swap(nums, mid, end);
                end--;
            }
        }
    }

    public static void main(String[] args)
    {
        int[] nums = new int[]{2,0,2,1,1,0};

        sort(nums);

        for(int i : nums)
        {
            System.out.print(i + " ");
        }
    }
}
